const RTL_LANGUAGES = ['ar', 'he', 'fa'];

// ===========================================
// TRANSLATION FUNCTIONS
// ===========================================

function getTranslation(key, lang) {
    lang = lang || 'en';
    const dict = VERITAS_TRANSLATIONS[lang] || VERITAS_TRANSLATIONS['en'];
    return dict[key] || VERITAS_TRANSLATIONS['en'][key] || key;
}

function applyTranslations(lang) {
    lang = lang || 'en';
    
    // Text content
    document.querySelectorAll('[data-i18n]').forEach(function(el) {
        var key = el.getAttribute('data-i18n');
        var t = getTranslation(key, lang);
        if (t) el.textContent = t;
    });
    
    // Placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
        var key = el.getAttribute('data-i18n-placeholder');
        var t = getTranslation(key, lang);
        if (t) el.placeholder = t;
    });
    
    // Titles
    document.querySelectorAll('[data-i18n-title]').forEach(function(el) {
        var key = el.getAttribute('data-i18n-title');
        var t = getTranslation(key, lang);
        if (t) el.title = t;
    });
    
    // RTL handling - ONLY affects text direction within elements, NOT page layout
    // We set the lang attribute but DO NOT set dir="rtl" on the document
    // This preserves the LCARS layout while allowing RTL text to display correctly
    if (RTL_LANGUAGES.indexOf(lang) !== -1) {
        document.body.classList.add('rtl-text');
        // Apply RTL direction only to translatable text elements, not the whole page
        document.querySelectorAll('[data-i18n]').forEach(function(el) {
            el.style.direction = 'rtl';
            el.style.textAlign = 'right';
        });
    } else {
        document.body.classList.remove('rtl-text');
        document.querySelectorAll('[data-i18n]').forEach(function(el) {
            el.style.direction = '';
            el.style.textAlign = '';
        });
    }
    
    document.documentElement.setAttribute('lang', lang);
    console.log('üåê VERITAS Universal Translator: ' + lang);
}

function initTranslator() {
    var selector = document.getElementById('languageSelect');
    var savedLang = localStorage.getItem('veritasLanguage') || 'en';
    
    if (selector) {
        selector.value = savedLang;
        selector.addEventListener('change', function() {
            localStorage.setItem('veritasLanguage', this.value);
            applyTranslations(this.value);
        });
    }
    
    applyTranslations(savedLang);
}

function setLanguage(lang) {
    if (VERITAS_TRANSLATIONS[lang]) {
        localStorage.setItem('veritasLanguage', lang);
        var selector = document.getElementById('languageSelect');
        if (selector) selector.value = lang;
        applyTranslations(lang);
        return true;
    }
    return false;
}

function getAvailableLanguages() {
    return Object.keys(VERITAS_TRANSLATIONS);
}

function getCurrentLanguage() {
    return localStorage.getItem('veritasLanguage') || 'en';
}

